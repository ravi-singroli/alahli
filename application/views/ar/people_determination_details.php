          <p>Al Ahli Driving Center is proud to teach students safe and easy driving in accordance with the UAE roads and the Roads and Transport Authority (RTA) regulations.</p>
          <p>Driving course for people with special needs uses specially adapted vehicles with hand controls for people with waist downwards disability. We have special Instructors for Deaf and Mute people trained from Sharjah Humanitarian Services.</p>
          <P>You will need to get a Medical report from any Hospital approved by the Dubai Health Authority.</P>
          <hr />
          <h3>Instructions and Information</h3>
          <ul class="disc">
            <li>The minimum age to register for light motor vehicle license is 17 years and 6 months and get your license after you complete 18 years.</li>
            <li>Minimum 20 hours practical training for light motor vehicle.</li>
            <li>Students who do not have any driving license are considered as "Beginner" and thus have to take minimum 20 hours practical training to get UAE driving license.</li>
            <li>Students having their country's license which is 2–5 years old have to take minimum 15 hours practical training to get UAE driving license.</li>
            <li>Students having their country's license which is more than 5 years old have to take minimum 10 hours practical training to get UAE driving license.</li>
          </ul>
          
