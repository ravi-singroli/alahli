          <h3> Heavy & Light Forklift </h3>
          <p>The Light Forklift and Heavy Forklift driving license course prepares the students for the RTA Road Test and enables them to get the Dubai Driving License. Students who complete this course and pass the RTA Road Tests are licensed to drive light forklifts and Heavy forklifts. Minimum age requirement for light forklift course is 20 years.</p>
          <p>The course offers an introduction to different types of forklifts. Detailed lessons on engine and loading capacity, the different types of levers along with their controls , how to operate in different working areas and in various weather conditions , focus on how to reduce maintenance expenses, increase efficiency, reduce accidents and improve operating skills, form part of the course. Students are taught in detail about load control and how to load various kinds of materials: at which angle will the load be adjustable.</p>
          <hr />
          <h3>Instructions and Information</h3>
          <ul class="disc">
            <li>The minimum age to obtain heavy and light forklift license is 20 years.</li>
            <li>Minimum 20 hours practical training for light and heavy Forklift.</li>
            <li>Light Forklifts criterion- Weight is less than 2.5 tons.</li>
            <li>Heavy forklifts criterion- weight is more than 2.5 tons.</li>
          </ul>
