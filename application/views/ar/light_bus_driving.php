          <h3> Light Bus Driving </h3>
          <p>The Light Bus driving license course prepares the students for the RTA driving test to get UAE Driving License category 5. Students who complete this course and pass the RTA Road Tests are licensed to drive passenger buses up to 26 seater.</p>
          <p>The driver should ensure that all their passengers, especially children are seated properly or are holding the handrails and he should know what to do in an emergency such as fire or crash.</p>
          <hr />
          <h3>Instructions and Information</h3>
          <ul class="disc">
            <li>The minimum age to obtain Light bus license is 20 years.</li>
            <li>Minimum 20 hours practical training for light Bus.</li>
            <li>After passing the required tests and receiving the license obtain a commercial driving permit for driver profession.</li>
          </ul>
         
