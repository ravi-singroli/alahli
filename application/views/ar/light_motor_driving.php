          <h3>Light motor vehicle</h3>
          <p>Al Ahli Driving Center is proud to teach students safe and easy driving in accordance with the UAE roads and the Roads and Transport Authority (RTA) regulations.</p>
          <p>This is a highly popular course and every aspect has been carefully conceived to instill confidence and practice safety, whether you are a beginner or looking to upgrade your driving skills. Whenever you are behind the wheel, the lessons will be carried out in modern dual-control cars configured with emergency controls.</p>
          <p>Our driving instructors are not only devoted to observing and maintaining safety at all times, they are exceptionally trained with years of driving experience. All our instructors are professional tutors with the requisite teaching license endorsed by the RTA. To enhance their skills, they frequently undergo training and briefings on the latest safety procedures and driving technology. </p>
          <hr />
          <h3>Instructions and Information</h3>
          <ul class="disc">
            <li>The minimum age to register for light motor vehicle license is 17year and 6 months and get your license after you complete       18years.</li>
            <li>Students who do not have any  driving license are considered as &quot;Beginner&quot; and thus have to take  minimum 20 hours practical training to get UAE driving license.</li>
            <li>Students having their country's  license which is 2–5 years old have to take minimum 15 hours practical training  to get UAE driving license.</li>
            <li>Students having their country's  license which is more than 5 years old have to take minimum 10 hours practical  training to get UAE driving license. </li>
          </ul>
