<script>
function frm_get_in_touch_submit(frm)
{
   //$("#loading").css('display','block');
    var form=$("#"+frm.id);
    $.ajax({
        type:"POST",
        url:form.attr("action"),
        data:form.serialize(),
        success: function(response)
        {
            if(response=="100")
            {
			   $("#txt_name2").val("");
			   $("#txt_email_id2").val("");
			   $("#txt_msg2").val("");
               $("#dv_error_touch").html("<p style='color:#fff'>Your request submitted successfully, We will contact you soon.</p>");
			   
            }			
			else
			{
				$("#dv_error_touch").html(response);
			}
			setTimeout(function(){$("#dv_error_touch").html('');},20000);
			// $("#loading").css('display','none');
        }
    });
    return false;
}
</script>